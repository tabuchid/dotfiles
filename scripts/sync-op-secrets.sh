#!/usr/bin/env bash
set -euo pipefail

ACCOUNT_DEFAULT="my.1password.com"
CACHE_FILE_DEFAULT="${XDG_CONFIG_HOME:-$HOME/.config}/dotfiles/secrets.1password.env.zsh"
REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
MANIFEST_FILE_DEFAULT="${REPO_DIR}/scripts/sync-op-secrets.manifest"

log() { printf "[op-secrets] %s\n" "$*"; }
warn() { printf "[op-secrets] WARNING: %s\n" "$*" >&2; }
die() { printf "[op-secrets] ERROR: %s\n" "$*" >&2; exit 1; }

need_cmd() { command -v "$1" >/dev/null 2>&1; }

usage() {
  cat <<'EOF'
Usage: sync-op-secrets.sh [--cache-file PATH] [--manifest PATH]

Fetches selected shell secrets from 1Password and writes a local zsh-compatible
cache file for shell startup. The cache file is outside the repo and chmod 600.

Run this after rotating a secret in 1Password.
EOF
}

cache_file="${CACHE_FILE_DEFAULT}"
manifest_file="${MANIFEST_FILE_DEFAULT}"

while [[ $# -gt 0 ]]; do
  case "$1" in
    --cache-file)
      [[ $# -ge 2 ]] || die "--cache-file requires a path"
      cache_file="$2"
      shift 2
      ;;
    --manifest)
      [[ $# -ge 2 ]] || die "--manifest requires a path"
      manifest_file="$2"
      shift 2
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      die "Unknown argument: $1"
      ;;
  esac
done

need_cmd op || die "1Password CLI (op) is not installed or not in PATH"
[[ -r "${manifest_file}" ]] || die "Manifest not found/readable: ${manifest_file}"

tmp_file="$(mktemp "${TMPDIR:-/tmp}/op-secrets.XXXXXX")"
cleanup() { rm -f "${tmp_file}"; }
trap cleanup EXIT
umask 077

mkdir -p "$(dirname "${cache_file}")"

{
  printf "# Generated by %s on %s\n" "$0" "$(date -u +'%Y-%m-%dT%H:%M:%SZ')"
  printf "# Source from zsh startup; refresh by running: %s\n\n" "$0"

  printf "# Manifest: %s\n\n" "${manifest_file}"

  while IFS='|' read -r env_name op_ref account; do
    [[ -z "${env_name}" ]] && continue
    [[ "${env_name}" =~ ^# ]] && continue
    [[ -n "${op_ref}" ]] || die "Missing 1Password reference for ${env_name}"
    [[ -n "${account}" ]] || account="${ACCOUNT_DEFAULT}"

    value="$(op read "${op_ref}" --account "${account}")"
    printf "export %s=%q\n" "${env_name}" "${value}"
  done < "${manifest_file}"
} > "${tmp_file}"

chmod 600 "${tmp_file}"
mv "${tmp_file}" "${cache_file}"

log "Wrote ${cache_file}"
log "Open a new shell (or run: source ${cache_file}) to load refreshed values"
